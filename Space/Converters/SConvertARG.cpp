const auto KEY_PATTERNS = std::list<std::regex>{
    std::regex("-([a-zA-Z])")
};
/**
 * ----------------------------------------------------------------------------
 * Public
 * ----------------------------------------------------------------------------
 */
Var Convert::FromARG(std::vector<std::string> args) {
    /**
     * init values
     */
    auto o = Map(); 
    auto k = Key(); 
    auto v = Obj(true); 
    /**
     * iterate args
     */
    for (auto& a : args) {
        /**
         * find key
         */
        for(auto& p : KEY_PATTERNS) {
            auto m = std::smatch();
            if (std::regex_match (a, m, p)) {
                /**
                 * found
                 */
                k = Key(m.str(1));
            }
        }
    
    }
}

