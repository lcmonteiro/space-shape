#ifndef STIME_H
#define STIME_H
/**
 * space
 */
#include "STypes.h"
/**
 * ------------------------------------------------------------------------------------------------
 * Time 
 * ------------------------------------------------------------------------------------------------
 */
class STime {
public:
    /**
     * ------------------------------------------------------------------------
     * Get GMT time
     * 
     * @return seconds from 1970 (unix system) 
     * ------------------------------------------------------------------------
     */
    static const Float Time();
    /**
     * ------------------------------------------------------------------------
     * Get system time
     * 
     * @return seconds from machine start
     * ------------------------------------------------------------------------
     */
    static const Float SysTime();
    /**
     * ------------------------------------------------------------------------
     * Convert time count to string (base on time zone)
     * @param time count
     * @param pattern
     * @return string
     * ------------------------------------------------------------------------
     */
    static const String ToDateTime(
        const Float&   time, 
        const String&  pattern = "%Y-%m-%d %X", 
              Boolean* dst     = nullptr
    );
    /**
     * ------------------------------------------------------------------------
     * Convert string to time count (base on time zone)
     * @param time
     * @param pattern
     * @return time count 
     * ------------------------------------------------------------------------
     */
    static const Float FromDateTime(
        const String& time,
        const String& pattern = "%Y-%m-%d %X", 
              Boolean dst     = false
    );
};
/**
 * ------------------------------------------------------------------------------------------------
 * End
 * ------------------------------------------------------------------------------------------------
 */
#endif /* STIME_H */

