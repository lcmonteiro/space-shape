#include <gtest/gtest.h>
/**
 * space
 */
#include "SPattern.h"
#include "SLog.h"
/**
 * -------------------------------------------------------------------------------------------------
 * Pattern - Merge
 * -------------------------------------------------------------------------------------------------
 */
TEST(Pattern, Merge)
{ 
    // settings ---------------------------------------------------------------

    // test 1 -----------------------------------------------------------------
    EXPECT_EQ(Tools::Pattern::Minimum("*abb","*annv"), "*annv");

    // test 2 -----------------------------------------------------------------
    EXPECT_EQ(Tools::Pattern::Merge("aa*cab", "ab*cc*ab"), "a?*c*ab");

    // test 3 -----------------------------------------------------------------
    EXPECT_TRUE(Tools::Pattern::Match("aa*cab",   "a?*c*ab"));
    EXPECT_TRUE(Tools::Pattern::Match("ab*cc*ab", "a?*c*ab"));
}
